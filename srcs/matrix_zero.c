#include "minirt.h"

void	zero_matrix44(t_matrix44 *matrix)
{
	int i;
	int j;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			if (i == j)
				matrix->m[i][j] = 1.0;
			else
				matrix->m[i][j] = 0;
			j++;
		}
		i++;
	}
}
